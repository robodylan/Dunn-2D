using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Dunn_2D.GUI
{
    class Text
    {
        public int x;
        public int y;
        public string text;

        public Text(int x, int y, string text)
        {
            this.x = x;
            this.y = y;
            this.text = text;
        }
    }
}
